int		ft_str_is_alpha(char *str)
{
	while (*str != '\0')
	{
		if (!((*str <= 'z' && *str >= 'a') || (*str <= 'Z' &&
						*str >= 'A')))
			return (0);
		str++;
	}
	return (1);
}
