int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (*str != '\0')
	{
		if (!((str[i] <= 'z' && str[i] >= 'a') || (str[i] <= 'Z' &&
						str[i] >= 'A')))
			return (0);
		str++;
	}
	return (1);
}
